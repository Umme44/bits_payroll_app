package com.bits.hr.service.xlExportHandling;

public class FBDeptWiseSummary {

    int headCount;
    double regularBonus;
    double contractualBonus;
    double totalBonus;

    public int getHeadCount() {
        return headCount;
    }

    public void setHeadCount(int headCount) {
        this.headCount = headCount;
    }

    public double getRegularBonus() {
        return regularBonus;
    }

    public void setRegularBonus(double regularBonus) {
        this.regularBonus = regularBonus;
    }

    public double getContractualBonus() {
        return contractualBonus;
    }

    public void setContractualBonus(double contractualBonus) {
        this.contractualBonus = contractualBonus;
    }

    public double getTotalBonus() {
        return totalBonus;
    }

    public void setTotalBonus(double totalBonus) {
        this.totalBonus = totalBonus;
    }
}
